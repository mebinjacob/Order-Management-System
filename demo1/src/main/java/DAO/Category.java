package DAO;

public class Category {
	private int id;
	private String name;
	private String subCategpry;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSubCategpry() {
		return subCategpry;
	}
	public void setSubCategpry(String subCategpry) {
		this.subCategpry = subCategpry;
	}
	
	
}
